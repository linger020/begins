while true; do
  show_menu
  read -r -p "Please enter your selection [0-16]: " choice
  case "$choice" in
    0) exit 0 ;;
    1) run_remote_script "$BASE_URL/init-server.sh"; pause ;;
    2) run_debian_official_source; pause ;;
    3) run_remote_script "$BASE_URL/host-ip.sh"; pause ;;
